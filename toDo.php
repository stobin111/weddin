<div><strong>Mississippi River-&nbsp;</strong></div>
<div>The Mississippi River, one of the five great river systems of the world, has always been a major feature of the Twin Cities. With its headwaters located in northern Minnesota, the river runs through the center of Minneapolis St Paul on its way to the Gulf of Mexico. It has been designated as a National River and Recreation Center. River drives wind along the river gorge on both sides and in both cities. The area flames with gorgeous color as the leaves turn with cooler temperatures.</div>
<div>&nbsp;</div>
<div><strong>Activities</strong> include walking, biking, driving tours, prominent attractions long its course. Rental bikes and rental cars are available.</div>
<div>&nbsp;</div>
<div>&nbsp;</div>
<div><strong>St. Croix River</strong></div>
<div>The St. Croix River forms much of the eastern border between Minnesota and Wisconsin. This pristine river has been designated as a National Scenic Waterway with several state parks on its banks. Stillwater is a city on its banks and is a east drive from the Twin Cities. Many of its historic buildings have been preserved and its downtown area is lined with restaurants and small shops. A city park stretches along the banks of the St. Croix River. Activities include walking, biking, shopping, numerous restaurants, sitting along the river banks, festivals. A 20 minute drive from the eastern suburbs.</div>
<div>
<div>&nbsp;</div>
<div><strong>Minnesota River</strong></div>
<div>The Minnesota River runs from the western border of Minnesota south east through some of the richest farming lands in the country (corn, soybeans, sugar beets) until it joins the Mississippi River in St. Paul. Many interesting towns and cities were built near its gorges.&nbsp;</div>
<div>&nbsp;</div>
<div><strong>Lake Superior</strong></div>
<div>Lake Superior forms the North Shore of Minnesota. It contains 10 percent of the worlds supply of fresh water with the Great Lakes system totaling 20 percent of the worlds fresh water. Lake Supper includes 20,364,800 acres of water.</div>
<div>The Lakes basin was formed by volcanic activity. The deepest spot in the lake at 1,333 feet. Duluth sits on the west end of Lake Superior. It features numerous outdoor activities four seasons of the year. The north shore has many state parks, hiking trails, wildlife viewing and roads leading further into the wilderness. Duluth is the largest seaport on the great lakes and an interesting mid continental seaport. Ships passing into its harbor area go through a canal</div>
<div>where people gather to watch the ships passing through.</div>
<div>&nbsp;</div>
<div>Average number of ships visiting the Duluth Harbor: 1000</div>
